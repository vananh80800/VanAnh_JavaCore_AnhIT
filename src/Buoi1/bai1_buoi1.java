package Buoi1;

public class bai1_buoi1 {
    public static void main(String[] args) {
        // Khai báo biến
        String name = "Nguyen Van A";   // Thay bằng tên của bạn
        int age = 20;                   // Thay bằng tuổi của bạn
        double height = 1.75;           // Thay bằng chiều cao của bạn (mét)
        boolean isStudent = true;       // true nếu bạn là sinh viên, false nếu không

        // In ra màn hình
        System.out.println("Tên: " + name);
        System.out.println("Tuổi: " + age);
        System.out.println("Chiều cao: " + height + " m");
        System.out.println("Có phải sinh viên không? " + isStudent);
    }
}
